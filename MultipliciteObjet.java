public class MultipliciteObjet {
    //99999999 = * ("infini")
    private int debut;
    private int fin;

    public MultipliciteObjet(int debut, int fin) {
        this.debut = debut;
        this.fin = fin;
    }

    public int getDebut() {
        return debut;
    }

    public void setDebut(int debut) {
        this.debut = debut;
    }

    public int getFin() {
        return fin;
    }

    public void setFin(int fin) {
        this.fin = fin;
    }
}
